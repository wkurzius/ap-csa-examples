/*
This program will output the text "Hello, world!",
as is customary when learning a new language.
*/
public class helloWorld {
    public static void main(String[] args) {

        int x = 100;

        x -= 5;
        System.out.println(x);

        x *= 5;
        System.out.println(x);

        x /= 5;
        System.out.println(x);

        x %= 5;
        System.out.println(x);

    }
}